$(document).ready(function(){

    $('.punct-circle-question').click(function(){
        $('.original-text').toggleClass('question-on');
    });

    $('.punct-circle-exclam').click(function(){
        $('.original-text').toggleClass('exclamation-on').toggleClass;
    });

    $('.punct-circle-quote').click(function(){
        $('.original-text').toggleClass('quotation-on');
    });

    $('.punct-circle-apos').click(function(){
        $('.original-text').toggleClass('apostrophe-on');
    });

    $('.punct-circle-period').click(function(){
        $('.original-text').toggleClass('period-on');
    });

    $('.punct-circle-comma').click(function(){
        $('.original-text').toggleClass('comma-on');
    });

    $('.punct-circle-colon').click(function(){
        $('.original-text').toggleClass('colon-on');
    });

    $('.punct-circle-scolon').click(function(){
        $('.original-text').toggleClass('semicolon-on');
    });

});